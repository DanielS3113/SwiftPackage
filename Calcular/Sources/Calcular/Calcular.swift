public struct Calcular {
    public init (){
        
    }
   
    
    public func suma (n1: Double, n2: Double ) {
        
        var sresultado: Double
        sresultado = n1 + n2
        print(sresultado)
    }
    public func resta (n1: Double, n2: Double ) {
        
        var sresultado: Double
        sresultado = n1 - n2
        print(sresultado)
    }
    public func multiplicacion (n1: Double, n2: Double ) {
        
        var sresultado: Double
        sresultado = n1 * n2
        print(sresultado)
    }
    public func division (n1: Double, n2: Double ) {
        
        var sresultado: Double
        sresultado = n1 / n2
        print(sresultado)
    }
}

